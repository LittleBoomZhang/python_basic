# 序列相乘（重复） -- 新序列 = 序列 * n（n必须是整数，不能是浮点数）
list1 = [10, 20, 30, 40]
list2 = list1 * 3
print(list2)

tuple1 = (10, 20, 30, 40)
tuple2 = tuple1 * 3
print(tuple2)

str1 = 'hello'
str2 = str1 * 6
print(str2)
